/* MEMORY MANAGEMENT

1) stack stores data in a proper structured manner while heaps stores data wherever it finds space

2) Dynamic data (array, strings, object etc.) is stored in heap
   Static data is stored in stack

3) All datatypes are stored in heap and their addresses are stored inside stack

4) true, false, null and undefined are allocated memory inside the heap as soon as the program starts (automatically)

5) when a memory adrress isn't pointed out by anyone in the heap, then the garbage collector deletes it.  
 

*/